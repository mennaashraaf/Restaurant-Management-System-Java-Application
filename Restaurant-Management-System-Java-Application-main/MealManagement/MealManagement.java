    String Taxs=String.format("%.2f$", iTaxs);
    jTextField17.setText(Taxs);
    jTextField17.setEditable(false);
    
    String Total=String.format("%.2f$", iTotal);
    jTextField19.setText(Total);
    jTextField19.setEditable(false);
