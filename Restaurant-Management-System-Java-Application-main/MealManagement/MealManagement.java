    obj.tortellini = obj.pTortellini*Double.parseDouble(jTextField14.getText());
    obj.frenchOnion = obj.pFrenchOnion*Double.parseDouble(jTextField15.getText());
    //declearing variables that will be used to get taxs&subtotal&total
    double iTaxs;
    double iSubTotal;
    double iTotal;
    iSubTotal=obj.getAmount();
    iTaxs=obj.getTaxs(iSubTotal);
    iTotal = iSubTotal+iTaxs;
